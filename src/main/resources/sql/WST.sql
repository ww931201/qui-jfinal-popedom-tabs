-- ----------------------------
CREATE OR REPLACE procedure "WST"."CREATE_WST_INFO"(sChart in string,sTrain in string) is
--根据图号和车次生成对应的车次信息五色图信息
chart_name varchar2(50);--图号汉字名称
zydd varchar2(50);--作业地点
cfNum number := 0;--用来判断是否有重复的作业；
cf_exception exception;--作业站重复，异常处理
zylx  varchar2(20);--作业类型
ccxx  varchar2(50);--车次信息

begin
  
select t.chart_name into chart_name from DATA_CHART t where t.chart_num=sChart;

  --删除已经存在的车次信息
  for ref in (       
      (select distinct t.left_scheme_name as scheme_name from DATA_CWZY_SCHEDULE t where t.scheme_id=(select t.id from DATA_CWZY_SCHEME t where t.train_code=sTrain and t.chart=sChart ))
      union
      (select distinct t.right_scheme_name as scheme_name from DATA_CWZY_SCHEDULE t where t.scheme_id=(select t.id from DATA_CWZY_SCHEME t where t.train_code=sTrain and t.chart=sChart ))
      )loop
        delete from DATA_WST_INFO d where d.train_info=ref.scheme_name and d.chart_num=sChart;
      end loop;
/

